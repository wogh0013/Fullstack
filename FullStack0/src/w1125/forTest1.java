package w1125;

public class forTest1 {
	public static void main(String[] args) {
		int i;
		
		for(i=1; i<20; i+=2) {
			System.out.print(i + " ");
		}
		System.out.println();
		
		for(i=2; i<=20; i+=2) {
			System.out.print(i + " ");
		}
		System.out.println();
		
		for(i=10; i>0; i-=1) {
			System.out.print(i + " ");
		}
		System.out.println();
	}
}
